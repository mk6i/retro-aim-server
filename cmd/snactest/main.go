package main

import (
	"bytes"
	"fmt"
	"os"

	"github.com/mk6i/retro-aim-server/wire"
)

func main() {
	b := []byte{
		0x02, 0x04, 0x05, 0x00, 0x18, 0x00, 0x01, 0x00, 0x00, 0x27, 0x11, 0xE9,
		0x06, 0x24, 0xD9, 0x3A, 0x86, 0x84, 0x54, 0x39, 0x7F, 0xF8, 0x1F, 0xAB,
		0xF0, 0x92, 0xCD, 0x00, 0xF1, 0xE5, 0x2E, 0xA2, 0x5E, 0x3A, 0x7B, 0x41,
		0x80, 0x42, 0xBA, 0xC1, 0x8B, 0x7B, 0x36, 0x80, 0x7F, 0xDA, 0xD7, 0xCF,
		0x9C, 0xAF, 0xF2, 0x56, 0x3C, 0x82, 0x85, 0xE1, 0xCF, 0xAC, 0x2F, 0x03,
	}
	flap := wire.FLAPFrame{}
	err := wire.UnmarshalBE(&flap, bytes.NewBuffer(b))
	if err != nil {
		fmt.Printf("flap err: %s", err.Error())
		os.Exit(1)
	}

	fmt.Printf("%+v\n\n", flap)

	snac := wire.SNACMessage{}
	err = wire.UnmarshalBE(&snac, bytes.NewBuffer(flap.Payload))
	if err != nil {
		fmt.Printf("snac err: %s", err.Error())
		os.Exit(1)
	}

	fmt.Printf("%+v\n\n", snac)
}
